// while : ittrate over the condition (entry control loop)

i = 1; // declaration
while (i < 10) {
	// condition check
	// if true so execute
	console.log(i);
	i++; // increment
}

// output
// 1
// 2
// 3
// 4
// 5
// 6
// 7
// 8
// 9



// do-while : ittrate over the condition (exit control loop) (first execute)

i = 11 // declaration
do {
    console.log(i);
    i++ // increment
} while (i <= 10)  // condition check
// ▶️▶️ print only 11 then exit the loop

// Because the condition is false